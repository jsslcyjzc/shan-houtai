		<p class="jie">品牌简介:</p>
		<p class="pins">努比亚（nubia）品牌成立于2012年10月31日，隶属努比亚技术有限公司（前身于2002年成立，2015年完成更名）。凭借单反级摄影功能被誉为“手机中的单反机”、“可以拍星星的手机“。产品以无边框设计，摄影，FiT交互，长续航和全网通等为特色，热销产品有努比亚miniS，无边框Z11等。品牌创始人里强任努比亚技术有限公司总裁，联合创始人倪飞任努比亚智能手机总经理。国际足坛巨星C•罗纳尔多任品牌代言人。</p>

		<p class="jie">品牌理念:</p>
		<p class="pins">努比亚坚持“Be Yourself”的品牌理念，在产品工艺、用户体验和技术研发等方面始终坚持创新与极致，同时也鼓励用户去追求自我和更优质的生活。在品牌成长的四年多时间内，努比亚凭借着手机摄影，无边框工艺，全网通等多项令人惊叹的技术及新一代无边框Z11、诚意“小牛6代”努比亚miniS等多款产品，不断践行着品牌主张。</p>

		<p class="jie">品牌含义:</p>
		<p class="pin">nubia，源于古文明的发源地之一，蕴义深刻丰富。在拉丁和部分语系中，nubia一词有“云彩”的语义，象征美好；nubia品牌赋予其新的释义和活力，预示着从古代文明向人类现代科技发展的跨越，见证nubia人对推动科技文明发展肩负的责任和努力。</p>

		<p class="jie">领导人简介:</p>
		<p class="pais">里强</p>
		<p class="pins">努比亚技术有限公司总裁、努比亚（nubia）品牌创始人。满族，出生于辽宁。中国摄影家协会会员、深圳企业家摄影协会理事，2016年中国手机产业年度风云人物。1996年毕业于哈尔滨工业大学并获博士学位。2000年加入中兴通讯股份有限公司，曾任中兴通讯海外区域总经理，2006年回国，先后出任深圳市中兴移动通信有限公司常务副总经理、总经理。2012年10月31日，里强与13位联合创始人一起创立了努比亚品牌。</p>
		<p class="pais">倪飞</p>
		<p class="pins">努比亚智能手机总经理、努比亚（nubia）品牌联合创始人，获2016年“新世代权力榜”之“创新人物奖”殊荣。2001年毕业于成都电子科技大学并加入中兴通讯。2004年转战手机行业，曾带领团队攻破欧洲高端市场，之后又将中兴手机成功带入北美高端市场。2012年开始，与努比亚技术有限公司总裁里强、以及其他12位联合创始人一起，共同创立了努比亚（nubia）品牌。</p>

		<p class="jie">创新技术:</p>
		<p class="pais">手机摄影</p>
		<p class="pin">手机摄影是努比亚品牌和产品的主打特点，努比亚自主研发的“NeoVision”影像引擎，拥有“微距相机”、“克隆相机”、“光绘”、“星轨”、“延时摄影”、“独立测光与对焦”、“独立白平衡“等诸多有趣的单反级摄影功能。</p>
		<p class="pais">基于aRC的无边框设计(Bezel-less design)</p>
		<p class="pin">努比亚推出的无边框手机，其实现原理是努比亚开创的aRC（arc Refractive Conduction）屏幕技术，该技术突破了LCD工艺的限制，将液晶显示面板中不可避免的封边进行视觉隐藏，为有边的LCD带来了无边的视觉效果，代表作品有nubia Z9、nubia Z11。</p>
		<p class="pais">FiT交互技术</p>
		<p class="pins">在无边框的设计基础上，努比亚创造了FiT交互技术。通过结构设计、底层驱动、手势操作算法，材料技术等多方面配合，简化了用户操控体验的深度和难度，通过简单的边缘滑动就可完成的诸如亮度调节、清理内存、截屏、握边拍摄等多种功能。FiT交互技术最先应用于nubia Z9，后续在布拉格，Z11 mini等多款产品也得以实现。随着新一代无边框旗舰nubia Z11的诞生，努比亚也带来了FiT交互技术2.0，对视效和交互进行了全面升级。</p>
		<p class="pais">NeoPower续航技术准则</p>
		<p class="pin">努比亚创新的NeoPower技术有效提升单位电量的续航能力，通过软硬件上的多重优化，在通话、导航、拍照、游戏、社交等十个用户日常最常使用场景入手，带来续航十项全能。</p>
		<p class="pais">全网通</p>
		<p class="pins">努比亚拥有全网通的相关专利。Z5 mini实现了当时的3G全网通；4G时代，nubia X6实现了4G+3G全网通；nubia Z7系列实现了7频17模全网通。nubia Z9系列更实现了7频18模全网通的网络支持。在全新Z11系列上，努比亚带来全网通3.0技术，新增了对VoLTE语音通话、双卡盲插、LTE+C、LTE+W等多种功能的支持。</p>

